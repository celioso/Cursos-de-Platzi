//suma, resta, multiplicacion, divicion//
function Suma(){
  resultado = 16 + 8;
  console.log(resultado);  
}

function Resta(){
  resultado = 16 - 8;
  console.log(resultado);  
}

function Multiplicación(){
  resultado = 16 * 8;
  console.log(resultado);  
}

function División(){
  resultado = 16 / 8;
  console.log(resultado);  
}

function Raiz(){
  resultado = Math.sqrt(16 + 8);
  console.log(resultado);  
}

function Módulo(){
  resultado = 16 % 8;
  console.log(resultado);  
}

Suma();
